// MARK: - FlashcardRowView
private struct FlashcardRowView: View {
    let flashcard: Flashcard
    var editAction: () -> Void
    
    var body: some View {
        HStack(spacing: 12) {
            // Ïπ¥Îìú ÎÇ¥Ïö©
            VStack(alignment: .leading, spacing: 6) {
                HStack {
                    Text(flashcard.front)
                        .font(.headline)
                        .fontWeight(.medium)
                    Spacer()
                    difficultyBadge
                }
                
                Text(flashcard.back)
                    .font(.subheadline)
                    .foregroundColor(.secondary)
                    .lineLimit(1)
                
                HStack(spacing: 12) {
                    Label("\(flashcard.reviewCount)", systemImage: "repeat")
                        .font(.caption)
                        .foregroundColor(.secondary)
                    
                    Label("Î∞ïÏä§ \(flashcard.boxNumber)", systemImage: "archivebox")
                        .font(.caption)
                        .foregroundColor(.secondary)
                }
            }
            
            // Ìé∏Ïßë Î≤ÑÌäº
            Button(action: editAction) {
                Image(systemName: "ellipsis.circle.fill")
                    .font(.title2)
                    .foregroundColor(.blue)
            }
            .buttonStyle(.borderless)
        }
        .padding(.vertical, 8)
        .contentShape(Rectangle())
    }
    
    @ViewBuilder
    private var difficultyBadge: some View {
        let (text, color) = difficultyInfo
        Text(text)
            .font(.caption)
            .fontWeight(.medium)
            .foregroundColor(.white)
            .padding(.horizontal, 8)
            .padding(.vertical, 4)
            .background(Capsule().fill(color))
    }
    
    private var difficultyInfo: (String, Color) {
        switch flashcard.difficulty {
        case 1: return ("Ïâ¨ÏõÄ", .green)
        case 2: return ("Î≥¥ÌÜµ", .orange)
        case 3: return ("Ïñ¥Î†§ÏõÄ", .red)
        default: return ("Î≥¥ÌÜµ", .gray)
        }
    }
}

